package lesson6.Calculator;

abstract public class Operation {
    private double a;
    private double b;

    public Operation(double a, double b) {
        this.a = a;
        this.b = b;
    }

    public double getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public double getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }

    abstract public void execute();
}
